    <h1>Install Laravel with Herd</h1>

    <ul>
        <li><strong>What is Herd</strong></li>
        <ul>
            <li>Herd is a lightweight local development environment for Laravel.</li>
            <li>It provides PHP, Nginx, and other tools preconfigured.</li>
            <li>It allows developers to run Laravel projects quickly without complex setup.</li>
        </ul>

        <li><strong>Install Herd</strong></li>
        <ul>
            <li>Download Laravel Herd from the official Laravel website.</li>
            <li>Install the application on your system.</li>
            <li>After installation, Herd automatically configures PHP and the web server.</li>
        </ul>

        <li><strong>Install Laravel Project</strong></li>
        <ul>
            <li>Open terminal.</li>
            <li>Run the command: <code>composer create-project laravel/laravel project-name</code></li>
            <li>Move to the project folder using <code>cd project-name</code>.</li>
        </ul>

        <li><strong>Test Install Project</strong></li>
        <ul>
            <li>Run the command: <code>php artisan serve</code></li>
            <li>Open browser and go to <code>http://127.0.0.1:8000</code></li>
            <li>If the Laravel welcome page appears, the installation is successful.</li>
        </ul>

        <li><strong>Make Changes in File</strong></li>
        <ul>
            <li>Open the project in your code editor.</li>
            <li>Go to <code>resources/views/welcome.blade.php</code>.</li>
            <li>Edit the content and refresh the browser to see changes.</li>
        </ul>

