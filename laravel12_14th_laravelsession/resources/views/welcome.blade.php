<div>
  <h1>Laravel Sessions – Notes</h1>
  <p>
    A session is used to store user-specific data that persists across multiple
    requests/pages during a single visit.
  </p>
  <ul>
    <li>Session data is stored on the server.</li>
    <li>The browser uses a cookie to identify the session.</li>
    <li>Helps maintain user state (login, cart, flash messages).</li>
  </ul>
